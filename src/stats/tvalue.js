// Retrieve t-value, using t-distribution, two-tailed, with 95% significance
// level.
export const getTvalue = function (degreesOfFreedom) {
  if (degreesOfFreedom >= T_VALUES.length) {
    return MIN_T_VALUE
  }

  return T_VALUES[degreesOfFreedom - 1]
}

